//PrepBuddy and Socks - PrepBuddy is getting late for college. She is looking for a matching pair of socks from a bos full of socks. In the worst case scenario, how many socks should
//Prepbuddy remove from the drawer until she finds a matching pair.
#include<bits/stdc++.h>
using namespace std;
int main() {
	int t;
	cin>>t;
	for(int i=1; i<=t; i++) {
		int n;
		cin>>n;
		n = n+1;
		cout<<n<<endl;
	}
	return 0;
}
